
a = 22
b = 4

print("%d +%d =%d"%(a,b,a+b))
print("%5d +%5d =%5d"%(a,b,a+b)) # 칸확보


a1 = 7
b1 = 3.56
c1 = "대박"
print("%d +%0.2f =%0.2f"%(a1,b1,a1+b1)) # 실수 소수점 2자리까지..
print("%5d +%5.2f =%5.2f"%(a1,b1,a1+b1)) # 실수 소수점 2
print("%5d +%5.2f =%5.2f %s"%(a1,b1,a1+b1,c1)) # 실수 소수점 2
